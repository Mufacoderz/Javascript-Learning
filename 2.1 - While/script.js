var nilaiAwal = 1; // Inisialisasi variabel nilai awal dengan 1.
while (nilaiAwal <= 5) { // Loop berjalan selama nilaiAwal <= 5.
    console.log("hello world"); // Menampilkan "hello world" di console.
    nilaiAwal++; // Menambahkan 1 ke nilaiAwal di setiap iterasi.
}


